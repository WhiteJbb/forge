def cmd_start(args: argparse.Namespace) -> int:
    try:
        load_config(args.config)
    except ConfigError as e:
        print(f"forge: {e}", file=sys.stderr)
        return 1

    from .server import create_app, print_banner  # 지연 import — 다른 커맨드는 서버 스택을 물지 않게

