_INIT_PROVIDER_TEMPLATES = [
    {
        "name": "nvidia",
        "env": "NVIDIA_API_KEY",
        "litellm_prefix": "openai",
        "api_base": "https://integrate.api.nvidia.com/v1",
    },
    {
        "name": "openrouter",
        "env": "OPENROUTER_API_KEY",
        "litellm_prefix": "openai",
        "api_base": "https://openrouter.ai/api/v1",
    },
    {
        "name": "anthropic",
        "env": "ANTHROPIC_API_KEY",
        "litellm_prefix": "anthropic",
        "api_base": None,
    },
]


def _provider_block_lines(tpl: dict) -> list[str]:
    lines = [f"  - name: {tpl['name']}", f"    litellm_prefix: {tpl['litellm_prefix']}"]
    if tpl["api_base"]:
        lines.append(f"    api_base: {tpl['api_base']}")
    lines.append(f"    api_key_env: {tpl['env']}")
    return lines


def _generate_yaml() -> tuple[str, list[str], bool]:
    """감지된 환경변수 기반 최소 forge.yaml 텍스트를 만든다.

    반환: (yaml 텍스트, 감지된 provider 이름 목록, 키가 하나도 없었는지 여부)
    """
    detected = [t for t in _INIT_PROVIDER_TEMPLATES if os.environ.get(t["env"])]
    no_keys = not detected

    if detected:
        block_lines: list[str] = []
        for t in detected:
            block_lines.extend(_provider_block_lines(t))
        providers_section = "providers:\n" + "\n".join(block_lines)
    else:
        # 키가 전혀 없으면 nvidia 블록을 주석 상태로만 안내 (providers는 빈 리스트로 유효하게 유지)
        example_lines = _provider_block_lines(_INIT_PROVIDER_TEMPLATES[0])
        commented = "\n".join(f"# {line}" for line in example_lines)
        providers_section = (
            "providers: []\n\n"
            "# No provider API key detected in environment\n"
            "# (NVIDIA_API_KEY / OPENROUTER_API_KEY / ANTHROPIC_API_KEY).\n"
            "# Set one, then uncomment and adjust a block like this under providers:\n"
            "#\n" + commented
        )

    yaml_text = (
        "# Forge config generated by `forge init` (schema: src/settings.py)\n"
        "version: 1\n\n"
        "server:\n"
        "  host: 127.0.0.1\n"
        "  port: 4000\n\n"
        "auth:\n"
        "  api_key_env: FORGE_API_KEY\n\n"
        f"{providers_section}\n\n"
        "models: []\n\n"
        "defaults:\n"
        "  capability: 7\n"
        "  tier: tier3\n"
        "  features: [tools, streaming]\n\n"
        "# 지출 제한 (유료 프로바이더 사용 시 권장) — 또는: forge guard --no-paid\n"
        "# policies:\n"
        "#   - name: spending-guard\n"
        "#     constraints: { allow_paid: false }\n"
    )
    return yaml_text, [t["name"] for t in detected], no_keys


def cmd_init(args: argparse.Namespace) -> int:
    target = Path(args.config)
    if target.exists() and not args.force:
        print(f"forge: {target} already exists (use --force to overwrite)", file=sys.stderr)
        return 1

    yaml_text, detected, no_keys = _generate_yaml()
    target.write_text(yaml_text, encoding="utf-8")

    try:
        load_config(target)
    except ConfigError as e:
        print(f"forge: generated config failed validation: {e}", file=sys.stderr)
        return 1

    if no_keys:
        print(f"Wrote {target} (no provider API key detected in environment).")
        print(
            "Set NVIDIA_API_KEY, OPENROUTER_API_KEY, or ANTHROPIC_API_KEY, then "
            "uncomment a provider block in the generated file."
        )
    else:
        print(f"Wrote {target} with provider(s): {', '.join(detected)}")
    print("Config validated OK.")
    print("Spend guard: forge guard --no-paid")
    return 0


