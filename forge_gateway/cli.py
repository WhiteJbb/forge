def build_parser() -> argparse.ArgumentParser:
    common = argparse.ArgumentParser(add_help=False)
    common.add_argument(
        "--config", default="forge.yaml", help="Path to forge.yaml (default: forge.yaml)"
    )

    parser = argparse.ArgumentParser(prog="forge", description="Forge AI Gateway CLI")
    sub = parser.add_subparsers(dest="command", required=True)

    sub.add_parser("start", parents=[common], help="Start the Forge server")

    p_init = sub.add_parser("init", parents=[common], help="Generate a starter forge.yaml")
    p_init.add_argument("--force", action="store_true", help="Overwrite an existing config file")

    sub.add_parser(
        "doctor", parents=[common], help="Diagnose config, API keys, and provider connectivity"
    )

    p_models = sub.add_parser(
        "models", parents=[common], help="List models known to the Registry (offline view)"
    )
    p_models.add_argument("--tier", choices=VALID_TIERS, help="Filter by tier")

    sub.add_parser(
        "reload", parents=[common], help="Hot-reload the running server's config (POST /admin/reload)"
    )

    p_guard = sub.add_parser(
        "guard", parents=[common], help="Manage the local spend guard (forge.local.yaml)"
    )
    guard_paid = p_guard.add_mutually_exclusive_group()
    guard_paid.add_argument(
        "--no-paid", action="store_true", help="Block paid (non-free) models"
    )
    guard_paid.add_argument(
        "--allow-paid", action="store_true", help="Remove the allow_paid restriction"
    )
    p_guard.add_argument(
        "--max-cost", type=float, metavar="USD", help="Set max_cost_per_request (USD)"
    )
    p_guard.add_argument("--off", action="store_true", help="Remove the local spend guard")

    sub.add_parser(
        "policies", parents=[common], help="List effective policies in evaluation order"
    )

    return parser


_COMMANDS = {
    "start": cmd_start,
    "init": cmd_init,
    "doctor": cmd_doctor,
    "models": cmd_models,
    "reload": cmd_reload,
    "guard": cmd_guard,
    "policies": cmd_policies,
}


def main(argv: Optional[list[str]] = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    return _COMMANDS[args.command](args)


if __name__ == "__main__":
    sys.exit(main())
