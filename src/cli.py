def _list_provider_models(
    pconf: ProviderConfig, timeouts: TimeoutsConfig
) -> tuple[bool, int, Optional[str]]:
    """make_provider + list_models() 호출을 실행한다.

    네트워크/구성 실패는 여기서 흡수해 (성공여부, 모델수, 에러요약) 튜플로 반환한다.
    테스트에서는 이 함수를 모킹해 네트워크 없이 doctor 로직을 검증한다.
    """
    try:
        provider = make_provider(pconf, timeouts)
    except Exception as e:  # noqa: BLE001 — provider 생성 실패도 진단 결과로 흡수
        return False, 0, f"{type(e).__name__}: {e}"

    async def _run() -> list[str]:
        try:
            return await provider.list_models()
        finally:
            await provider.close()

    try:
        models = asyncio.run(_run())
    except Exception as e:  # noqa: BLE001
        return False, 0, f"{type(e).__name__}: {e}"
    return True, len(models), None


def _check_provider(pconf: ProviderConfig, timeouts: TimeoutsConfig) -> dict:
    key_configured = pconf.api_key_env is not None
    key_present = bool(pconf.api_key) if key_configured else False

    ok, count, error = _list_provider_models(pconf, timeouts)

    problem = (key_configured and not key_present) or not ok

    return {
        "name": pconf.name,
        "api_key_env": pconf.api_key_env or "-",
        "key_configured": key_configured,
        "key_present": key_present,
        "list_models_ok": ok,
        "model_count": count,
        "error": error,
        "problem": problem,
    }


def cmd_doctor(args: argparse.Namespace) -> int:
    try:
        config = load_config(args.config)
    except ConfigError as e:
        print(f"forge: {e}", file=sys.stderr)
        return 1

    print(f"Config: OK ({args.config})")
    print()

    if not config.providers:
        print("No providers configured.")
        return 0

    results = [_check_provider(p, config.timeouts) for p in config.providers]

    header = "{:<15} {:<22} {:<8} {:<8} {:<8} {}".format(
        "PROVIDER", "KEY_ENV", "KEY_SET", "MODELS", "STATUS", "DETAIL"
    )
    print(header)
    print("-" * len(header))

    any_problem = False
    for r in results:
        if r["problem"]:
            any_problem = True
        if r["key_configured"]:
            key_set = "yes" if r["key_present"] else "no"
        else:
            key_set = "n/a"
        status = "FAIL" if r["problem"] else "OK"
        detail = r["error"] or ""
        print(
            "{:<15} {:<22} {:<8} {:<8} {:<8} {}".format(
                r["name"], r["api_key_env"], key_set, r["model_count"], status, detail
            )
        )

    print()
    if any_problem:
        print("Overall: FAIL - see DETAIL column above.")
        return 1
    print("Overall: OK")
    return 0


