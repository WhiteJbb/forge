def cmd_models(args: argparse.Namespace) -> int:
    try:
        config = load_config(args.config)
    except ConfigError as e:
        print(f"forge: {e}", file=sys.stderr)
        return 1

    registry = Registry(config)
    entries = registry.all()
    if args.tier:
        entries = [e for e in entries if e.tier == args.tier]
    entries.sort(key=lambda e: (e.tier, e.id))

    header = "{:<45} {:<7} {:<28} {:<10} {}".format(
        "ID", "TIER", "FEATURES", "CONTEXT", "PRICE ($/1Mtok in,out)"
    )
    print(header)
    print("-" * len(header))
    for e in entries:
        features = ",".join(sorted(e.features))
        context = str(e.context_window) if e.context_window else "-"
        if e.price_per_mtok is None:
            price = "unknown"
        else:
            price = f"{e.price_per_mtok[0]:.2f}/{e.price_per_mtok[1]:.2f}"
        print("{:<45} {:<7} {:<28} {:<10} {}".format(e.id, e.tier, features, context, price))

    print()
    print(f"{len(entries)} model(s). Runtime status (health/cooldown) is at the server's /health.")
    return 0


